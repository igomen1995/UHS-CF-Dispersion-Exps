function props = getMixtureProps_REFPROP( ...
    RP,...
    fluidString,...
    z,...
    T,...
    P)

% fluidString example:
%   'HELIUM*XENON'
%
% z:
%   [0.25 0.75]
%
% T [K]
% P [kPa]

% REFPROP expects up to 20 mole fractions

zREF = zeros(1,20);

zREF(1:length(z)) = z;

zREF = py.list(num2cell(zREF));

% Density

out = RP.REFPROPdll( ...
    fluidString,...
    'TP',...
    'D',...
    int32(0),...
    int32(0),...
    int32(0),...
    T,...
    P,...
    zREF);

props.rho = double(out.Output(1));

end
